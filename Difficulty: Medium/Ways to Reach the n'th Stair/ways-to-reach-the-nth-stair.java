class Solution
{
    //Function to count number of ways to reach the nth stair.
    int countWays(int n)
    {
        
        // your code here
        int MOD=1000000007;
        int prev2=0;
        int prev1=1;
        for(int i=1;i<=n;i++)
        {
            int curr=(prev2+prev1)%MOD;
            prev2=prev1;
            prev1=curr;
        }
        return prev1;
    }
}

